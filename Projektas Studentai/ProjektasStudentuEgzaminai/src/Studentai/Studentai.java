package Studentai;

public class Studentai {

    private int id;
    private String name;
    private String lastName;

    public Studentai(int iD, String name, String lastName) {
        this.id = id;
        this.name = name;
        this.lastName = lastName;
    }

    public int getiD() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getLastName() {
        return lastName;
    }
}
